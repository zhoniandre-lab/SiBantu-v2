create policy "users read own profile" on public.profiles for select using (auth.uid() = id);
create policy "users update own profile" on public.profiles for update using (auth.uid() = id);
create policy "customers read own carts" on public.carts for select using (auth.uid() = customer_id);
create policy "customers read own conversations" on public.conversations for select using (auth.uid() = customer_id);
create policy "customers read own addresses" on public.addresses for select using (auth.uid() = customer_id);
create policy "customers read own orders" on public.orders for select using (auth.uid() = customer_id);

create policy "public reads active stores" on public.stores for select using (status = 'active');
create policy "sellers read own stores" on public.stores for select using (auth.uid() = owner_id);
create policy "sellers update own stores" on public.stores for update using (auth.uid() = owner_id);
create policy "users submit seller applications" on public.seller_applications for insert with check (auth.uid() = applicant_id);
create policy "users read own seller applications" on public.seller_applications for select using (auth.uid() = applicant_id);
create policy "members read own membership" on public.store_members for select using (auth.uid() = profile_id);

create policy "public reads approved products" on public.products for select using (
  is_active = true and moderation_status = 'approved'
);
create policy "sellers create products in own store" on public.products for insert with check (
  exists (select 1 from public.stores s where s.id = store_id and s.owner_id = auth.uid())
);
create policy "sellers update products in own store" on public.products for update using (
  exists (select 1 from public.stores s where s.id = store_id and s.owner_id = auth.uid())
);
create policy "public reads active variants" on public.product_variants for select using (
  is_active = true and exists (
    select 1 from public.products p where p.id = product_id and p.is_active = true and p.moderation_status = 'approved'
  )
);
create policy "sellers manage variants in own store" on public.product_variants for all using (
  exists (
    select 1 from public.products p
    join public.stores s on s.id = p.store_id
    where p.id = product_id and s.owner_id = auth.uid()
  )
);
create policy "sellers read own store orders" on public.store_orders for select using (
  exists (select 1 from public.stores s where s.id = store_id and s.owner_id = auth.uid())
);
create policy "sellers update own store orders" on public.store_orders for update using (
  exists (select 1 from public.stores s where s.id = store_id and s.owner_id = auth.uid())
);
create policy "sellers read own payouts" on public.seller_payouts for select using (
  exists (select 1 from public.stores s where s.id = store_id and s.owner_id = auth.uid())
);
create policy "customers create store reviews" on public.store_reviews for insert with check (auth.uid() = customer_id);
create policy "public reads store reviews" on public.store_reviews for select using (true);

